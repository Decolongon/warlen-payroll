    }

    /**
     * Get or create a position by name (case-insensitive)
     */
    private function getOrCreatePosition(string $posName): Position
    {
        if (empty(trim($posName))) {
            $posName = 'Regular Employee';
        }
        $posName = trim($posName);
        $slug    = Str::slug($posName);

        $position = Position::whereRaw('LOWER(pos_name) = ?', [strtolower($posName)])
            ->orWhere('pos_slug', $slug)
            ->first();

        if ($position) {
            if (strtolower($position->pos_name) !== strtolower($posName)) {
                $position->pos_name = $posName;
                $position->save();
            }
            return $position;
        }

        return Position::create([
            'pos_name'     => $posName,
            'pos_slug'     => $slug,
            'basic_salary' => 550,
        ]);
    }

    /**
     * Get or create a site by name under the given branch
     */
    private function getOrCreateSite(string $siteName, Branch $branch): Site
    {
        $siteName = empty(trim($siteName)) ? 'Default Site' : trim($siteName);

        $site = Site::where('branch_id', $branch->id)
            ->whereRaw('LOWER(site_name) = ?', [strtolower($siteName)])
            ->first();

        if ($site) {
            return $site;
        }

        return Site::create([
            'site_name' => $siteName,
            'branch_id' => $branch->id,
        ]);
    }

