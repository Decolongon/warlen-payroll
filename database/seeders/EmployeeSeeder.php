        $spreadsheetEmployees = $this->parseEmployeeData();
        $dynamicUsers         = [];

        foreach ($spreadsheetEmployees as $emp) {
            $dynamicUsers[] = [
                'name'            => $emp['name'],
                'email'           => $emp['email'],
                'emp_id'          => $emp['emp_code'],
