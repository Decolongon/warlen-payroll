    private function getOrCreateUser(array $userData, array $roles): User
    {
        $user = User::where('email', $userData['email'])->first();

        if (!$user) {
            $user = User::create([
                'name'               => $userData['name'],
                'email'              => $userData['email'],
                'password'           => Hash::make('12345678'),
                'email_verified_at'  => now(),
            ]);
            $user->assignRole($roles[$userData['role']]);
        } elseif (!$user->hasRole($userData['role'])) {
            $user->assignRole($userData['role']);
        }

        return $user;
    }

    private function mapGender(?string $gender): ?string
    {
        if (empty($gender)) return null;
        $g = strtoupper(trim($gender));
        if ($g === 'M') return 'male';
        if ($g === 'F') return 'female';
        return strtolower($gender);
    }

    private function createEmployeeRecord(
        Position $position,
        User     $user,
        Branch   $branch,
        int      $empId,
        ?Site    $site   = null,
        array    $extra  = []
    ): void {
        if ($user->hasRole('admin')) {
            $this->command->info("Skipping employee record for admin: {$user->name}");
            return;
        }

        // Ensure unique emp_code
        $empCode = $empId;
        if (Employee::where('emp_code', $empCode)->exists()) {
            $this->command->warn("Duplicate emp_code {$empCode} for {$user->name}, generating new code.");
            do {
                $empCode = rand(1000000, 9999999);
            } while (Employee::where('emp_code', $empCode)->exists());
        }

        $validStatuses = ['active', 'end_of_contract', 'awol', 'terminated', 'resigned', 'newly_hired'];
        $rawStatus     = $extra['employee_status'] ?? '';
        $empStatus     = in_array($rawStatus, $validStatuses) ? $rawStatus : 'active';

        Employee::firstOrCreate(
            ['user_id' => $user->id],
            [
                'position_id'               => $position->id,
                'branch_id'                 => $branch->id,
                'user_id'                   => $user->id,
                'site_id'                   => $site?->id,
                'slug_emp'                  => Str::slug($user->name . '-' . $empCode),
                'emp_code'                  => $empCode,
                'employee_number'           => $this->generateEmployeeNumber(),
                'sss_number'                => $extra['sss_number']        ?? $this->generateSssNumber(),
                'pagibig_number'            => $extra['pagibig_number']     ?? $this->generatePagibigNumber(),
                'philhealth_number'         => $extra['philhealth_number']  ?? $this->generatePhilhealthNumber(),
                'contract_start_date'       => $extra['contract_start_date'] ?? now(),
                'contract_end_date'         => $extra['contract_end_date']   ?? now()->addYear(),
                'emergency_contact_number'  => $extra['contact_person_number'] ?? '09123456789',
                'pay_frequency'             => 'weekender',
                'employee_status'           => $empStatus,
                'contact_person'            => $extra['contact_person']     ?? null,
                'contact_person_number'     => $extra['contact_person_number'] ?? null,
                'skills'                    => !empty($extra['skills'])
                    ? array_filter(array_map('trim', explode(',', $extra['skills'])))
                    : null,
                'gender'                    => $this->mapGender($extra['gender'] ?? null) ?? 'male',
                'dob'                       => $extra['dob']               ?? null,
                'mother_name'               => $extra['mother_name']       ?? null,
                'father_name'               => $extra['father_name']       ?? null,
                'educ_attainment'           => $extra['educ_attainment']   ?? null,
                'certificate'               => $extra['certificate']       ?? null,
                'permanent_address'         => $extra['permanent_address'] ?? null,
                'present_address'           => $extra['present_address']   ?? null,
                'duration'                  => null,
            ]
        );
    }

    private function displayStatistics(): void
    {
        $this->command->newLine();
        $this->command->info('========================================');
        $this->command->info('Employee Seeder completed successfully!');
        $this->command->info('========================================');
        $this->command->info('Admin users:           ' . User::role('admin')->count());
        $this->command->info('HR users:              ' . User::role('hr_head')->count());
        $this->command->info('Employee users:        ' . User::role('employee')->count());
        $this->command->info('Total employee records:' . Employee::count());
        $this->command->info('========================================');
    }

    public function run(): void
    {
        $roles = [
            'admin'    => Role::firstOrCreate(['name' => 'admin']),
            'employee' => Role::firstOrCreate(['name' => 'employee']),
            'hr_head'  => Role::firstOrCreate(['name' => 'hr_head']),
        ];

        $branch          = $this->getBranch();
        $defaultPosition = $this->getOrCreatePosition('Regular Employee');
        $defaultSite     = $this->getOrCreateSite('Default Site', $branch);

        $usersData  = $this->getUsersData();
        $totalUsers = count($usersData);

        $this->command->info('Starting Employee Seeder...');
        $this->command->info("Total users to process: {$totalUsers}");

        $progressBar = $this->command->getOutput()->createProgressBar($totalUsers);
        $progressBar->start();

        $processedUsers = [];

        foreach ($usersData as $data) {
            $extraAttrs = $data['employee_attrs'] ?? [];
            $user       = $this->getOrCreateUser($data, $roles);

            $position = !empty($extraAttrs['position_name'])
                ? $this->getOrCreatePosition($extraAttrs['position_name'])
                : $defaultPosition;

            $site = !empty($extraAttrs['site_name'])
                ? $this->getOrCreateSite($extraAttrs['site_name'], $branch)
                : $defaultSite;

            $processedUsers[] = [
                'user'        => $user,
                'emp_id'      => $data['emp_id'],
                'role'        => $data['role'],
                'position'    => $position,
                'site'        => $site,
                'extra_attrs' => $extraAttrs,
            ];

            $progressBar->advance();
        }

        $progressBar->finish();
        $this->command->newLine(2);

        $nonAdminUsers = array_filter($processedUsers, fn($item) => $item['role'] !== 'admin');
        $nonAdminCount = count($nonAdminUsers);

        $this->command->info("Creating employee records for {$nonAdminCount} non-admin users...");
        $empBar = $this->command->getOutput()->createProgressBar($nonAdminCount);
        $empBar->start();

        foreach ($processedUsers as $item) {
            $this->createEmployeeRecord(
                $item['position'],
                $item['user'],
                $branch,
                $item['emp_id'],
                $item['site'],
                $item['extra_attrs']
            );
            if ($item['role'] !== 'admin') {
                $empBar->advance();
            }
        }

        $empBar->finish();
        $this->displayStatistics();
    }
