    /**
     * Parse employee data into seeder-ready format
     */
    private function parseEmployeeData(): array
    {
        $raw     = $this->getEmployeeData();
        $results = [];

        static $missingCounter = 0;

        foreach ($raw as $e) {
            $name = trim($e['name']);
            if (empty($name)) {
                continue;
            }

            // Build email
            $email = !empty($e['email']) ? trim($e['email']) : null;
            if (!$email) {
                $emailBase = strtolower(str_replace([' ', ',', '.', "'"], ['.', '', '', ''], $name));
                $email     = $emailBase . '@example.com';
            }

            // emp_code from id_number column; generate negative placeholder for missing ones
            $empCode = !empty($e['id_number']) ? (int) $e['id_number'] : null;
            if (!$empCode) {
                $missingCounter--;
                $empCode = $missingCounter;
            }

            // Map status → enum
            $validStatuses = ['active', 'end_of_contract', 'awol', 'terminated', 'resigned', 'newly_hired'];
            $status        = strtolower(trim($e['status'] ?? ''));
            if (!in_array($status, $validStatuses)) {
                $status = 'active';
            }

            // Contract start
            $contractStart = !empty($e['start_date'])
                ? Carbon::parse($e['start_date'])
                : now();

            // Contract end
            $contractEnd = !empty($e['date_end'])
                ? Carbon::parse($e['date_end'])
                : $contractStart->copy()->addYear();

            $results[] = [
                'name'                  => $name,
                'email'                 => $email,
                'emp_code'              => $empCode,
                'employee_status'       => $status,
                'gender'                => $e['sex'] ?? null,
                'position_name'         => $e['position'] ?? null,
                'site_name'             => $e['project_site'] ?? null,
                'permanent_address'     => $e['permanent_address'] ?? null,
                'present_address'       => $e['present_address'] ?? null,
                'contact_number'        => $e['contact_number'] ?? null,
                'dob'                   => !empty($e['dob']) ? Carbon::parse($e['dob'])->format('Y-m-d') : null,
                'contact_person'        => $e['contact_person'] ?? null,
                'contact_person_number' => $e['contact_number2'] ?? null,
                'mother_name'           => $e['mother_name'] ?? null,
                'father_name'           => $e['father_name'] ?? null,
                'sss_number'            => !empty($e['sss']) ? $e['sss'] : null,
                'philhealth_number'     => !empty($e['philhealth']) ? $e['philhealth'] : null,
                'pagibig_number'        => !empty($e['pagibig']) ? $e['pagibig'] : null,
                'tin_number'            => $e['tin'] ?? null,
                'contract_start_date'   => $contractStart->format('Y-m-d'),
                'contract_end_date'     => $contractEnd->format('Y-m-d'),
                'educ_attainment'       => $e['educ'] ?? null,
                'certificate'           => $e['certificates'] ?? null,
                'skills'                => $e['skills'] ?? null,
            ];
        }

        return $results;
    }

    private function getUsersData(): array
    {
        $staticUsers = [
            ['name' => 'Warlito', 'email' => 'warlito@gmail.com',  'emp_id' => 1000, 'role' => 'admin'],
            ['name' => 'Elena',   'email' => 'elena@gmail.com',    'emp_id' => 1001, 'role' => 'admin'],
